/* to show failcode cnt infos */
VOID WX_FailCode_DbgShow(char *argv[], UINT32 argc)
{
    wx_show("FailCode CNT INFOS");
	wx_show("-------------");
	wx_show("%-64s %s", "FailCode", "CntValue");
    UINT64 cntValue = 0;
	for (UINT32 i = 0; i < WX_FAIL_CODE_BUTT; i++) {
        cntValue = g_wxfailCodeCnt[i];
		if (cntValue == 0) {
			continue;
		}
		if (g_wxFailCodeDesc[i] == NULL) {
			/*
			* Undefinedi	ivalue
			*/
			wx_show("%-64u %lu", i, cntValue);
		} else {
			wx_show("%-64s %lu", g_wxFailCodeDesc[i], cntValue);
		}
		
	}
	wx_show("--- End");
}

